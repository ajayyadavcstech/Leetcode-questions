class Solution{
public:
    vector<long long> smallerSum(int n,vector<int> &arr){
        // Code here
        map<int,vector<int>> mp;
        for(int i=0;i<n;i++) mp[arr[i]].push_back(i);
        vector<long long> ans(n,0);
        long long sum = 0;
        for(auto &x:mp){
            for(auto &i:x.second) ans[i] = sum;
            sum = sum + 1ll*x.second.size()*x.first;
        }
        return ans;
    }
};

