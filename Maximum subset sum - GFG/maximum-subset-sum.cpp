class Solution {

  public:
    long long findMaxSubsetSum(int N, vector<int> &A) {
        // code here
        int take = 0;
        int not_take = 0;
        for(int i=0;i<N;i++){
            int cur_take = A[i] + max(take,not_take);
            int cur_nottake = take;
            take = cur_take;
            not_take = cur_nottake;
        }
        return max(take,not_take);
    }
};


